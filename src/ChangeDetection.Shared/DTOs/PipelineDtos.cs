namespace ChangeDetection.Shared.Dtos;

/// <summary>
/// Request to run the watch setup pipeline.
/// </summary>
public class RunPipelineRequest
{
    /// <summary>
    /// User's natural language input describing what to watch.
    /// </summary>
    public required string Input { get; set; }

    /// <summary>
    /// Maximum iterations for selector validation. Defaults to 3.
    /// </summary>
    public int? MaxIterations { get; set; }

    /// <summary>
    /// Minimum confidence threshold (0-1). Defaults to 0.8.
    /// </summary>
    public double? ConfidenceThreshold { get; set; }
}

/// <summary>
/// Response from the watch setup pipeline.
/// </summary>
public class RunPipelineResponse
{
    /// <summary>
    /// Whether the pipeline completed successfully.
    /// </summary>
    public bool IsSuccess { get; set; }

    /// <summary>
    /// Current stage of the pipeline.
    /// </summary>
    public string Stage { get; set; } = string.Empty;

    /// <summary>
    /// Number of iterations performed.
    /// </summary>
    public int IterationCount { get; set; }

    /// <summary>
    /// Extracted URLs from the input.
    /// </summary>
    public List<string> ExtractedUrls { get; set; } = [];

    /// <summary>
    /// Best selector candidate found.
    /// </summary>
    public SelectorCandidateDto? BestSelector { get; set; }

    /// <summary>
    /// All selector candidates considered.
    /// </summary>
    public List<SelectorCandidateDto> AllSelectors { get; set; } = [];

    /// <summary>
    /// LLM's analysis of the page content.
    /// </summary>
    public ContentAnalysisDto? ContentAnalysis { get; set; }

    /// <summary>
    /// Ready-to-use watch configuration.
    /// </summary>
    public ParsedWatchRequestDto? WatchConfig { get; set; }

    /// <summary>
    /// Error message if pipeline failed.
    /// </summary>
    public string? ErrorMessage { get; set; }

    /// <summary>
    /// Detailed logs from each stage.
    /// </summary>
    public List<string> Logs { get; set; } = [];
}

/// <summary>
/// A selector candidate generated by the pipeline.
/// </summary>
public class SelectorCandidateDto
{
    /// <summary>
    /// Type of selector: XPath, CssSelector, Regex.
    /// </summary>
    public required string Type { get; set; }

    /// <summary>
    /// The selector expression.
    /// </summary>
    public required string Expression { get; set; }

    /// <summary>
    /// Confidence score (0-1).
    /// </summary>
    public double Confidence { get; set; }

    /// <summary>
    /// Whether this selector has been validated.
    /// </summary>
    public bool IsValidated { get; set; }

    /// <summary>
    /// Number of matches found during validation.
    /// </summary>
    public int MatchCount { get; set; }

    /// <summary>
    /// Sample of extracted text from validation.
    /// </summary>
    public string? SampleText { get; set; }

    /// <summary>
    /// LLM's explanation of why this selector was chosen.
    /// </summary>
    public string? Reasoning { get; set; }
}

/// <summary>
/// LLM's analysis of the page content.
/// </summary>
public class ContentAnalysisDto
{
    /// <summary>
    /// Type of page detected (e.g., EventListing, ProductPage, NewsArticle).
    /// </summary>
    public string? PageType { get; set; }

    /// <summary>
    /// LLM's understanding of what the user wants to monitor.
    /// </summary>
    public string? UserIntent { get; set; }

    /// <summary>
    /// Key content sections identified on the page.
    /// </summary>
    public List<ContentSectionDto> ContentSections { get; set; } = [];

    /// <summary>
    /// Recommended monitoring approach.
    /// </summary>
    public string? RecommendedApproach { get; set; }
}

/// <summary>
/// A content section identified on the page.
/// </summary>
public class ContentSectionDto
{
    /// <summary>
    /// Name of the section.
    /// </summary>
    public required string Name { get; set; }

    /// <summary>
    /// Description of what this section contains.
    /// </summary>
    public string? Description { get; set; }

    /// <summary>
    /// Suggested selector for this section.
    /// </summary>
    public string? SuggestedSelector { get; set; }

    /// <summary>
    /// Relevance score (0-1) to user's intent.
    /// </summary>
    public double Relevance { get; set; }
}

